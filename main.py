import discord
from discord.ext import commands
from config import settings

#------------------------------------------------------------------------------#
#----------------------------  Основной код  ----------------------------------#
#------------------------------------------------------------------------------#

bot = commands.Bot(command_prefix = settings['prefix'])

@bot.event
async def on_ready():
    print("[BOT] - Запущен")

@bot.command()
async def new_windows(ctx):
    await ctx.send('Привет')



#------------------------------------------------------------------------------#
#-------------------------------  Запуск  -------------------------------------#
#------------------------------------------------------------------------------#

bot.run(settings['token'])

